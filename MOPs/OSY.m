function [z, c] = OSY(x)
    
    z1 = -(25 * (x(1) - 2)^2 + (x(2) - 2)^2 + (x(3) - 1)^2 + (x(4) - 4)^2+ (x(5) - 1)^2);
    
    z2 = sum(x.^2);
    
    z = [z1 z2]';
    
    c1 = x(1) + x(2) - 2;
    
    c2 = 6 - x(1) - x(2);
    
    c3 = 2 - x(2) + x(1);
    
    c4 = 2 - x(1) + 3 * x(2);
    
    c5 = 4 - (x(3) - 3)^2 - x(4);
    
    c6 = (x(5) - 3)^2 + x(6) - 4;
    
    c = -[c1 c2 c3 c4 c5 c6]';
    
    c = max(c, 0);
      
end